

-- Array

  - Can stored multiple types

> test = {"x": ['a', 1, new Date()]}
{ "x" : [ "a", 1, ISODate("2018-06-11T18:19:40.084Z") ] }

  - Index can also be created for an array
    . For example, in the above array we can create an index on array 'x'
    
  - Atomic Updates
    . MongoDB supports atomic updates on an Array


